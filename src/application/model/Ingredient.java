package application.model;

public class Ingredient {
	
	private String name;
	
	private String title;
	
	private double amount;
	
	private String unit;
	
	public String getName() {
		return this.name;
	}
	
	public String getTitle() {
		return this.title;
	}

	public double getAmount() {
		return amount;
	}
	
	public String getUnit() {
		return unit;
	}
}
